package demo.reports;

public class User
{
	private String age;

	private int id;

	private String name;

	private String sex;

	public String getAge()
	{
		return age;
	}

	public int getId()
	{
		return id;
	}

	public String getName()
	{
		return name;
	}

	public String getSex()
	{
		return sex;
	}

	public void setAge(String age)
	{
		this.age = age;
	}

	public void setId(int id)
	{
		this.id = id;
	}

	public void setName(String name)
	{
		this.name = name;
	}

	public void setSex(String sex)
	{
		this.sex = sex;
	}

}
